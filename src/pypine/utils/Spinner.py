class Spinner(object):

	_CHARACTERS = "|/-\\"

	################################################################################################################################
	## Constructor
	################################################################################################################################

	#
	# Constructor method.
	#
	@jk_typing.checkFunctionSignature()
	def __init__(self):
		self.__n = 0
	#

	################################################################################################################################
	## Public Properties
	################################################################################################################################

	################################################################################################################################
	## Helper Methods
	################################################################################################################################

	################################################################################################################################
	## Public Methods
	################################################################################################################################

	def show(self):
		print(Spinner._CHARACTERS[self.__n] + "\r")
		self.__n = (self.__n + 1) % 4
	#

	def hide(self):
		print(" ")
	#

